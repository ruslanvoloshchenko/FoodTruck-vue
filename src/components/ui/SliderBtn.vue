<template>
  <label class="switch">
    <input type="checkbox" v-model="value" />
    <span class="slider round"></span>
  </label>
</template>

<script>
export default {
  name: 'SliderBtn',
  emits: ['update:modelValue'],
  props: {
    modelValue: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    value: {
      get() {
        return this.modelValue
      },
      set(value) {
        this.$emit('update:modelValue', value)
      }
    }
  }
}
</script>

